    private void btnThemLuongCoBanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemLuongCoBanActionPerformed
        String maLuongCoBan = txtMaLuongCoBan.getText();
        String ml = txtMucLuong.getText();
        Float mucLuong = Float.parseFloat(ml);
        DateFormat df = new SimpleDateFormat("yyyy-MM-dd");
        String ngayHIeuLuc = df.format(dtpNgayHieuLuc.getDate());
        String ngayHetHieuLuc = df.format(dtpNgayHetHieuLuc.getDate());

        if (maLuongCoBan.equals("") || mucLuong.equals("")
                || ngayHIeuLuc.equals("") || ngayHetHieuLuc.equals("")) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin!");
        } else {
            DAOLuong luongcoban = new DAOLuong();
            try {
                luongcoban.themLuongCoBan(maLuongCoBan, ngayHIeuLuc, ngayHetHieuLuc, mucLuong);
                JOptionPane.showMessageDialog(this, "Thêm Lương cơ bản thành công!");
            } catch (SQLException ex) {
                //Logger.getLogger(DangKyJPanel.class.getName()).log(Level.SEVERE, null, ex);
            }

            hienThiLuongCoBan();
        }
    }//GEN-LAST:event_btnThemLuongCoBanActionPerformed

