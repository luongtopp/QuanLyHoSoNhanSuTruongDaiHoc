    private void tblLuongCoBanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblLuongCoBanMouseClicked
        int row = tblLuongCoBan.getSelectedRow();
        String maLuongCoBan = tblLuongCoBan.getValueAt(row, 0).toString();
        String NHL = tblLuongCoBan.getValueAt(row, 1).toString();
        // Định dạng chuỗi ngày/tháng từ bảng thành đối tượng Date
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Date ngayHieuLuc = null;
        try {
            ngayHieuLuc = sdf.parse(NHL);
        } catch (ParseException ex) {
            Logger.getLogger(CapNhapQuaTrinhCanBo.class.getName()).log(Level.SEVERE, null, ex);
        }

        String NHHL = tblLuongCoBan.getValueAt(row, 2).toString();
        Date ngayHetHieuLuc = null;
        try {
            ngayHetHieuLuc = sdf.parse(NHHL);
        } catch (ParseException ex) {
            Logger.getLogger(CapNhapQuaTrinhCanBo.class.getName()).log(Level.SEVERE, null, ex);
        }
        String mucLuong = tblLuongCoBan.getValueAt(row, 3).toString();

        txtMaLuongCoBan.setText(maLuongCoBan);
        dtpNgayHieuLuc.setDate(ngayHieuLuc);
        dtpNgayHetHieuLuc.setDate(ngayHetHieuLuc);
        txtMucLuong.setText(mucLuong);

    }//GEN-LAST:event_tblLuongCoBanMouseClicked

